    private RestTemplate gateWayRestTemplate() {
        RestTemplate bean = restTemplate1;
        // 设置编码为UTF-8
        bean.getMessageConverters().set(1, new StringHttpMessageConverter(StandardCharsets.UTF_8));
        bean.setUriTemplateHandler(new UriTemplateHandler() {
            @Override
            public URI expand(String uriTemplate, Map<String, ?> uriVariables) {
                return wrapGatewayUri(uriTemplate);
            }

            @Override
            public URI expand(String uriTemplate, Object... uriVariables) {
                return wrapGatewayUri(uriTemplate);
            }
        });
        bean.setInterceptors(Collections.singletonList((request, body, execution) -> {
            //增加网关的鉴权头部
            if(isGateWayUrl(request.getURI().toString())){
                GatewaySecurityUtil.setManagerApiGatewaySecurityHeaders(request,accessToken);
            }else{
                //门户需要特殊处理，门户可以不走网关 直接调用业务的接口
                setManagerApiGatewaySecurityHeaders(request,accessToken);
            }
            // 服务或服务事项收藏/取消息收藏清除缓存
            String url = request.getURI().getPath();
            if(url.contains("serviceFavorite/favoriteServiceItem") || url.contains("itemManager/favoriteItem")
                    || url.contains("itemManager/batchFavoriteItem")){
                String userId = userUtil.getUserAccount();
                String partKey = "userId:" + userId  + ":cardId:";
//                String allServiceKey = "allService:userId:" + userId ;
//                String allServiceItemKey = "allServiceItem:userId:" + userId ;
                Set<String> keys = redisUtil.scan(partKey);
//                keys.addAll(redisUtil.scan(allServiceKey));
//                keys.add(allServiceItemKey);
                redisUtil.deleteKeys(keys);
            }
//            if (HttpMethod.POST.equals(request.getMethod())) {
//                request.getHeaders().setContentType(MediaType.APPLICATION_JSON);
//            }
            return execution.execute(request, body);
        }));
        bean.setRequestFactory(clientHttpRequestFactory());
        return bean;
    }

    /**
     * 增加网关的鉴权头部
     * 门户需要特殊处理，门户可以不走网关 直接调用业务的接口
     */
    public static void setManagerApiGatewaySecurityHeaders(HttpRequest request,String token){
        GatewaySecurityUtil.setManagerApiGatewaySecurityHeaders(request,token);

        long timestamp = System.currentTimeMillis();
        String iv = RandomStringUtils.randomAlphanumeric(6);
        String baseIv = EncryptUtil.encryptBase64(iv);

        String path = RegExUtils.replaceAll(request.getURI().getPath(),"//","/");
        request.getHeaders().add("wisedu-minos-timestamp", String.valueOf(timestamp));
        request.getHeaders().add("wisedu-minos-iv", iv);
        request.getHeaders().add("wisedu-minos-uri", path);
        request.getHeaders().add("wisedu-minos-sign", EncryptUtil.SHA256(schoolSerial + path+ baseIv+ timestamp));
    }

    private HttpComponentsClientHttpRequestFactory clientHttpRequestFactory() {
        try {
            HttpClientBuilder httpClientBuilder = HttpClientBuilder.create();
            SSLContext sslContext = new SSLContextBuilder().loadTrustMaterial(null, new TrustStrategy() {
                public boolean isTrusted(X509Certificate[] arg0, String arg1) throws CertificateException {
                    return true;
                }
            }).build();
            httpClientBuilder.setSSLContext(sslContext);
            HostnameVerifier hostnameVerifier = NoopHostnameVerifier.INSTANCE;
            SSLConnectionSocketFactory sslConnectionSocketFactory = new SSLConnectionSocketFactory(sslContext,
                    hostnameVerifier);
            Registry<ConnectionSocketFactory> socketFactoryRegistry = RegistryBuilder.<ConnectionSocketFactory>create()
                    .register("http", PlainConnectionSocketFactory.getSocketFactory())
                    .register("https", sslConnectionSocketFactory).build();// 注册http和https请求
            // 开始设置连接池
            PoolingHttpClientConnectionManager poolingHttpClientConnectionManager = new PoolingHttpClientConnectionManager(
                    socketFactoryRegistry);
            poolingHttpClientConnectionManager.setMaxTotal(100000); // 最大连接数100
            poolingHttpClientConnectionManager.setDefaultMaxPerRoute(10000); // 同路由并发数100
            httpClientBuilder.setConnectionManager(poolingHttpClientConnectionManager);
            httpClientBuilder.setRetryHandler(new DefaultHttpRequestRetryHandler(3, true)); // 重试次数
            HttpClient httpClient = httpClientBuilder.build();
            HttpComponentsClientHttpRequestFactory clientHttpRequestFactory = new HttpComponentsClientHttpRequestFactory(
                    httpClient); // httpClient连接配置
            clientHttpRequestFactory.setConnectTimeout(2000); // 连接超时
            clientHttpRequestFactory.setReadTimeout(10000); // 数据读取超时时间
            clientHttpRequestFactory.setConnectionRequestTimeout(2000);
            return clientHttpRequestFactory;
        } catch ( KeyManagementException | NoSuchAlgorithmException | KeyStoreException e) {
            logger.error("初始化HTTP连接池出错", e);
        }
        return null;
    }

    private static boolean isGateWayUrl(String url){
        return org.apache.commons.lang3.StringUtils.startsWith(url,gateWayAddress);
    }

    private static URI wrapGatewayUri(String uri) {
        if (!uri.startsWith("http")) {
            if (uri.startsWith("/")) {
                uri = gateWayAddress + uri;
            } else {
                uri = gateWayAddress + "/" + uri;
            }
        }
        try {
            URI uri1 = new URI(uri);
            logger.debug("请求url地址：{}", uri1.toString());
            return uri1;
        } catch (URISyntaxException e) {
            throw new BusinessException("", e);
        }
    }


