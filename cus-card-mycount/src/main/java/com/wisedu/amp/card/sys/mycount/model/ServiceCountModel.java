package com.wisedu.amp.card.sys.mycount.model;

public class ServiceCountModel {

    private int itemCount;

    private int onlineItemCount;

    private int deptCount;

    private double appraiseMark;

    public int getItemCount() {
        return itemCount;
    }

    public void setItemCount(int itemCount) {
        this.itemCount = itemCount;
    }

    public int getOnlineItemCount() {
        return onlineItemCount;
    }

    public void setOnlineItemCount(int onlineItemCount) {
        this.onlineItemCount = onlineItemCount;
    }

    public int getDeptCount() {
        return deptCount;
    }

    public void setDeptCount(int deptCount) {
        this.deptCount = deptCount;
    }

    public double getAppraiseMark() {
        return appraiseMark;
    }

    public void setAppraiseMark(double appraiseMark) {
        this.appraiseMark = appraiseMark;
    }
}
