/**
 * 所有服务对象
 */
public class RoleModel {

    private String roleWid;

    private String roleName;

    private String showRoleName;

    private String isRelate;

    public String getRoleWid() {
        return roleWid;
    }

    public void setRoleWid(String roleWid) {
        this.roleWid = roleWid;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getShowRoleName() {
        return showRoleName;
    }

    public void setShowRoleName(String showRoleName) {
        this.showRoleName = showRoleName;
    }

    public String getIsRelate() {
        return isRelate;
    }

    public void setIsRelate(String isRelate) {
        this.isRelate = isRelate;
    }
}
